DROP TABLE IF EXISTS FAQ;

CREATE TABLE FAQ
(ID BIGINT AUTO_INCREMENT PRIMARY KEY NOT NULL,
 QUESTION VARCHAR(255) NOT NULL DEFAULT '',
 ANSWER VARCHAR(255) NOT NULL DEFAULT '');

